    boolean kiemtra_LuongPhanTT(int n, int x_dem[], int y_dem[], int t_dem[], int temp_y, int temp_t) {
        int temp_x = 0;
        x_dem[temp_x] = 0;
        boolean tt = false;
        int temp_yy = 0;
        int temp_tt = 0;
        for (int h = 0; h < n; h++) {
            if (tt) {
                temp_t = temp_yy;
            }
            tt = true;
            for (int i = 0; i < temp_t; i++) {
                temp_x++;
                if (dem_i(i, x_dem, temp_x - 1)) {
                    x_dem[temp_x] = t_dem[i];
                }
            }
            for (int i = 0; i <= temp_x; i++) {
                for (int j = 0; j < t; j++) {
                    if (pO.lk_Point[x_dem[i]][j] == 1) {
                        if (dem_i(i, y_dem, temp_yy)) {
                            y_dem[temp_yy] = j;
                            temp_yy++;
                        }

                    }
                }
            }
            for (int i = 0; i < temp_yy; i++) {
                for (int j = 0; j <= t; j++) {
                    if (pO.lk_Point[y_dem[i]][j] == 1) {
                        if (dem_i(i, t_dem, temp_tt)) {
                            t_dem[temp_tt] = j;
                            temp_tt++;
                        }

                    }
                }
            }
            for (int i = 0; i < temp_tt; i++) {
                for (int j = 0; j < temp_yy; j++) {
                    if (y_dem[j] == t_dem[i]) {
                        return false;
                    }
                }
            }
        }
        return true;
    }

