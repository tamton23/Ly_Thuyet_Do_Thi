    //resize char
    void resize_char() {
        lbchar_Show1.setText(char_Resize1.getValue() + "");

        if (char_Resize1.getValue() > 28 || char_Resize1.getValue() < 10) {
            JOptionPane.showMessageDialog(this, "khổ chữ ko hợp lệ");
            char_Resize1.setValue(18);
            resize_char();
        } else {
            Font font = new Font("Monospaced", Font.BOLD, char_Resize1.getValue());
            txtA_temp.setFont(font);
            txtA_ShowResuilt.setFont(font);

        }
        char_Resize1.setValue(char_Resize1.getValue());
    }

    //method resize
    void Resize_boll_Run() {
        lbSl_Show.setText(sL_Resize.getValue() + "");
        this.pO.resize = sL_Resize.getValue();
        if (t > 0) {
            if (temp_resize >= sL_Resize.getValue()) {
                this.pO.del_boll(temp_resize);
            }
            this.pO.Resize_boll();
            temp_resize = sL_Resize.getValue();

            if (sL_Resize.getValue() <= 10) {
                JOptionPane.showMessageDialog(null, "Kích thước quá nhỏ");
                sL_Resize.setValue(40);
            }
        }
    }

